#! /usr/bin/bash
#_______________________________________________________________________
# Minimal script to build and run Ada program
#_______________________________________________________________________

echo "________________________________________________________________"
echo " //\\o.o/\\\\    //\\o.o/\\\\    //\\o.o/\\\\    //\\o.o/\\\\    //\\o.o/\\\\"
echo "________________________________________________________________"

echo " Build project"
echo "________________________________________________________________"
echo ""

gnatmake hello.adb -o a.out

if [[ -f "*.ali" ]]; then
  rm *ali
fi

if [[ -f "*.o" ]]; then
  rm *.o
fi


echo "________________________________________________________________"
echo " Run executable"
echo "________________________________________________________________"
echo ""
./a.out

echo ""
echo "________________________________________________________________"
echo " Delete executable"

# Delete executable
rm a.out

echo "________________________________________________________________"
echo " //\\o.o/\\\\    //\\o.o/\\\\    //\\o.o/\\\\    //\\o.o/\\\\    //\\o.o/\\\\"
echo "________________________________________________________________"
